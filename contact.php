			$(document).ready(function(){
				$(".gototop").click(function(){
					$('html, body').animate({scrollTop : 0},400);
					return false;
				});

				$.ajax({
					type: "GET",
					url: "index_function.php",
					success: function(data){
						obj = JSON.parse(data);
						if(obj.gallery.slideone){
							sub = obj.gallery.slideone;
							subset = sub.substring(3);
							$("#slideone").attr("src", subset);
						}
						if(obj.gallery.slidetwo){
							sub = obj.gallery.slidetwo;
							subset = sub.substring(3);
							$("#slidetwo").attr("src", subset);
						}
						if(obj.gallery.slidethree){
							sub = obj.gallery.slidethree;
							subset = sub.substring(3);
							$("#slidethree").attr("src", subset);
						}
					}
				});	

				$.ajax({
					type: "GET",
					url: "index_function.php",
					success: function(data){
						obj = JSON.parse(data);
						$(".row-news").prepend(obj.postnews);
						$(".row-events").prepend(obj.postevents);
						$("#read").click(function(e){
							e.preventDefault();
							href = $(this).attr("href");
							$('html, body').animate({
						        scrollTop: $(href).offset().top
						    }, 500);
						});
					}
				});	

				//contact us
				$("#contactusform").submit(function(e){
					e.preventDefault();
					var sendername = $("#contactname").val();
					var senderemail = $("#emailsender").val();
					var sendermsg = $("#sendermsg").val();

					if( $.trim(sendername) == ""){
						$("#contactname").removeClass("valid").addClass("invalid");
					}else{
						$("#contactname").removeClass("invalid").addClass("valid");
					}

					if( $.trim(senderemail) == ""){
						$("#emailsender").removeClass("valid").addClass("invalid");
					}else{
						$("#emailsender").removeClass("invalid").addClass("valid");
					}

					if( $.trim(sendermsg) == ""){
						$("#sendermsg").removeClass("valid").addClass("invalid");
					}else{
						$("#sendermsg").removeClass("invalid").addClass("valid");
					}

					if( !$(".contactinputbox").hasClass("invalid") ){

						$.ajax({
							type: "POST",
							url: "index_function.php",
							data: $("#contactusform").serializeArray(),
							beforeSend: function(){
								$("#contactmsgsend").html("Sending...");
							},
							success: function(data){
								obj = JSON.parse(data);

								if(obj.emailcontactsend == true){
									$("#contactmsgsend").html("Sending...");
									$("#contactsuccess").openModal();
								}
							}
						});
					}

				});

			});
